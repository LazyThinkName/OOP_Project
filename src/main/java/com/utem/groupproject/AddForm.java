    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        
        int foodNum = 0,drinkNum = 0;
        int currFoodNum,currDrinkNum;
        
        try {
            currFoodNum = manageFood.readAllFood().size();
            foodNum = currFoodNum;
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            currDrinkNum = manageDrink.readAllDrink().size();
            drinkNum = currDrinkNum;
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if (!isEmpty())
        {
            if (arrivalDateChooser.getDate().compareTo(expiredDateChooser.getDate()) > 0){
                JOptionPane.showMessageDialog(null,"Arrival Date cannot be greater than Expired Date","Alert",JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (foodBtn.isSelected() == true){
                foodNum++;
                food = new Food(){};
                food.setItemID(foodNum);
                food.setItemType();
                food.setItemName(nameTxtField.getText().trim());
                food.setArrivalDate(sdf.format(arrivalDateChooser.getDate()));
                food.setExpiredDate(sdf.format(expiredDateChooser.getDate()));
                food.setItemPrice(Double.parseDouble(priceTxtField.getText().trim()));
                food.setItemQuantity(Integer.parseInt(qtyTxtField.getText().trim()));
                
                try {
                    manageFood.saveFood(food);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(AddForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            else
            {
                drinkNum++;
                drink = new Drink(){};
                drink.setItemID(drinkNum);
                drink.setItemType();
                drink.setItemName(nameTxtField.getText().trim());
                drink.setArrivalDate(sdf.format(arrivalDateChooser.getDate()));
                drink.setExpiredDate(sdf.format(expiredDateChooser.getDate()));
                drink.setItemPrice(Double.parseDouble(priceTxtField.getText().trim()));
                drink.setItemQuantity(Integer.parseInt(qtyTxtField.getText().trim()));
                
                try {
                    manageDrink.saveDrink(drink);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(AddForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            JOptionPane.showMessageDialog(null,"The item has been saved!");
            clearForm();
        }
        else
        {
            JOptionPane.showMessageDialog(null,"All the fields are required","Alert",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_addBtnActionPerformed

